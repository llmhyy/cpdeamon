package model.intermediate;

public class ClassVariable {
int label;
String token;
String type;
public int getLabel() {
	return label;
}
public void setLabel(int label) {
	this.label = label;
}
public String getToken() {
	return token;
}
public void setToken(String token) {
	this.token = token;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
}
